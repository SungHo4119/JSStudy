/**
 * Variable 선언하기
 *
 * 1) var - 더이상 쓰지 않는다.
 * 2) let
 * 3) const
 */
var name = 'Name';
console.log(name);

var age = 32;
console.log(age);

/**
 * let과 var로 선언하면
 * 값을 추후 변경 할 수 있다.
 */
age = 33;
console.log(age);

const newJeans = '뉴진스';
console.log(newJeans);

// newJeans = '뉴진스2';

/**
 * 선언과 할당
 * 1) 변수를 선언하는 것.
 *   - let name;
 * 2) 할당
 *   - let name = 'Name'
 */

let girlFriend;
console.log(girlFriend);

